package spring.user.domain3;

public class User {
	String id ;
	String name;
	String password;
	
	public String getId() {
		return id;
	}
	public void setId(String Id) {
		this.id = Id ;
	}
	public String getName() {
		return name;
	}
	public void setName(String Name) {
		name = Name ;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String Password) {
		password = Password ;
	}

}
